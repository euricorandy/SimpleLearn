package umn.ac.simplelearn;

public class putPDF {

    public String name;
    public String url;

    public putPDF() {
    }

    public putPDF(String name, String url) {
        this.name = name;
        this.url = url;
    }

    public String getName() {
        return name;
    }

    public String getUrl() {
        return url;
    }
}
